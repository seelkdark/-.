def калькулятор():
    print("Введите математическое выражение для вычисления (или 'выход' для выхода):")
    while True:
        выражение = input(">>> ")
        if выражение.lower() == 'выход':
            print("Выход из калькулятора.")
            break
        try:
            результат = eval(выражение)
            print("Результат:", результат)
        except Exception as e:
            print("Ошибка:", e)

if __name__ == "__main__":
    калькулятор()